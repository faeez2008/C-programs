#include <stdio.h>

int main() {
    int a = 10;    // Basic assignment
    printf("a = %d\n", a);
    
    a += 5;        // Compound: adds 5 to a
    printf("a += 5: %d\n", a);
    
    a *= 2;        // Compound: multiplies a by 2
    printf("a *= 2: %d\n", a);
    
    return 0;
}
